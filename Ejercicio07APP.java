

public class Ejercicio07APP {
	public static void main(String[] args) {

		// Esta variable nos servirá para ir subiendo des del 1 al 100.
		int i = 1;

		// En el while le hemos indicado que se repita mientras 1 sea más pequeña o
		// igual que 100.
		while (i <= 100) {
			// Estas dos líneas nos muestran por pantalla el número actual de i y luego le
			// suman 1
			System.out.println(i);
			i++;
		}

	}
}
